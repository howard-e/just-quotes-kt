package com.heed.justquotes.models

/**
 * @author Howard.
 */

class ForismaticQuote {

    val quoteText: String? = null
    val quoteAuthor: String? = null
    val senderName: String? = null
    val senderLink: String? = null
    val quoteLink: String? = null

    override fun toString(): String {
        return "ForismaticQuote(quoteText=$quoteText, quoteAuthor=$quoteAuthor, senderName=$senderName, senderLink=$senderLink, quoteLink=$quoteLink)"
    }
}
